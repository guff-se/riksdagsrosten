<h1 style="margin-left:0px;">Riksdagsrösten - ett stätt att bevaka och påverka riksdagen</h1>
<p>Anmäl dig <a href="https://docs.google.com/a/guff.se/spreadsheet/viewform?formkey=dFB0WTcwRmtJUmd5U2tlT3d4Uk9pY3c6MQ">Riksdagsrösten för Organisationer BETA.</a>

<p>Under hösten kommer Riksdagsrösten att introducera organisationekonton, för de intresseorganisationer, partier, föreningar eller byråer som vill uttrycka sin åsikt i de frågor som våra riksdagsledamöter röstar om. Dessa kommer att fungera som vanliga användarkonton, där man får möjlighet att följa och läsa in sig på de utskottsförslag som går till votering och ge sin röst. När ni röstar med ert organsationskonto kommer er organisations logotyp att dyka upp på voteringssidan och ni får på detta sätt möjlighet att påverka våra användare i den stund de lägger sin röst. Det kommer även vara möjligt att kommentera er ståndpunkt.</p>
